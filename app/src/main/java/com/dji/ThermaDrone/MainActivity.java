    @Override
    public void onMapClick(LatLng point) {
        if (isAdd == true){
            markWaypoint(point);
            Waypoint mWaypoint = new Waypoint(point.latitude, point.longitude, altitude);
            //Add Waypoints to Waypoint arraylist;
            if (waypointMissionBuilder != null) {
                waypointList.add(mWaypoint);
                waypointMissionBuilder.waypointList(waypointList).waypointCount(waypointList.size());
            }else
            {
                //No mission have started yet, HERE WE SET THE FIRST POINT
                waypointMissionBuilder = new WaypointMission.Builder();
                //FIRST WAYPOINT
                waypointList.add(mWaypoint);
                waypointMissionBuilder.waypointList(waypointList).waypointCount(waypointList.size());
            }
        }else{
            setResultToToast("Cannot Add Waypoint");
        }
    }

    public static boolean checkGpsCoordination(double latitude, double longitude) {
        return (latitude > -90 && latitude < 90 && longitude > -180 && longitude < 180) && (latitude != 0f && longitude != 0f);
    }

    // Update the drone location based on states from MCU.
    private void updateDroneLocation(){

        LatLng pos = new LatLng(droneLocationLat, droneLocationLng);
        //Create MarkerOptions object
        final MarkerOptions markerOptions = new MarkerOptions();
        markerOptions.position(pos);
        markerOptions.icon(BitmapDescriptorFactory.fromResource(R.drawable.aircraft));

        runOnUiThread(new Runnable() {
            @Override
            public void run() {
                if (droneMarker != null) {
                    droneMarker.remove();
                }

                if (checkGpsCoordination(droneLocationLat, droneLocationLng)) {
                    droneMarker = gMap.addMarker(markerOptions);
                }
            }
        });
    }

    private void markWaypoint(LatLng point){
        //Create MarkerOptions object
        MarkerOptions markerOptions = new MarkerOptions();
        markerOptions.position(point);
        markerOptions.icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_BLUE));
        Marker marker = gMap.addMarker(markerOptions);
        mMarkers.put(mMarkers.size(), marker);
    }
    private void markWaypointHOMEPOINT(LatLng point){
        //Create MarkerOptions object For the Home Marker!
        MarkerOptions markerOptions = new MarkerOptions();
        markerOptions.position(point);
        markerOptions.icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_YELLOW));
        Marker marker = gMap.addMarker(markerOptions);
        mMarkers.put(mMarkers.size(), marker);
    }

    public void batt(){
        try {
            DJIDemoApplication.getProductInstance().getBattery().setStateCallback(new BatteryState.Callback() {
                @Override
                public void onUpdate(BatteryState djiBatteryState) {
                    stringBuffer.delete(0, stringBuffer.length());

                    stringBuffer.append("Batt: ").
                            append(djiBatteryState.getChargeRemainingInPercent());

                    bv = djiBatteryState.getChargeRemainingInPercent();
                    String bvs = String.valueOf(djiBatteryState.getChargeRemainingInPercent());

                    batterystatus.setText("Batt: " + bvs + "%");
                    if(bv >= 75){
                        batterystatus.setBackgroundColor(getResources().getColor(R.color.green));
                    }else if(bv < 75 || bv >= 25){
                        batterystatus.setBackgroundColor(getResources().getColor(R.color.yellow));
                    }else if(bv < 25){
                        batterystatus.setBackgroundColor(getResources().getColor(R.color.red));
                    }

                    //batterystatus.setText(stringBuffer.toString());
                }
            });
        } catch (Exception ignored) {

        }
    }

    @Override
    public void onClick(View v) {
        switch (v.getId()) {
            case R.id.locate:{
                updateDroneLocation();
                cameraUpdate(); // Locate the drone's place
                //Call the method that return to me the value of the battery everytime that it updates!
                batt();

                locate.setTextColor(getResources().getColor(R.color.green));

                break;
            }
            case R.id.add:{
                enableDisableAdd();

                add.setTextColor(getResources().getColor(R.color.green));

                break;
            }
            case R.id.clear:{
                if(waypointMissionBuilder != null) {
                    runOnUiThread(new Runnable() {
                        @Override
                        public void run() {
                            gMap.clear();
                        }

                    });
                    waypointList.clear();
                    waypointMissionBuilder.waypointList(waypointList);
                    waypointMissionBuilder = null;
                    updateDroneLocation();
                }

                locate.setTextColor(getResources().getColor(R.color.colorBlack));
                add.setTextColor(getResources().getColor(R.color.colorBlack));
                config.setTextColor(getResources().getColor(R.color.colorBlack));
                upload.setTextColor(getResources().getColor(R.color.colorBlack));

                break;
            }
            case R.id.config:{
                showSettingDialog();

                config.setTextColor(getResources().getColor(R.color.green));

                break;
            }
            case R.id.upload:{
                uploadWayPointMission();
                upload.setTextColor(getResources().getColor(R.color.green));
                break;
            }
            case R.id.start:{
                if(bv <= 15){
                    Toast.makeText(MainActivity.this, "Please charge battery above 15%", Toast.LENGTH_SHORT).show();
                }else{
                    startWaypointMission();
                }

                //Activity for thermal streaming
                //Intent intent = new Intent(this, WebViewActivity.class);
                //startActivity(intent);
                break;
            }
            case R.id.stop:{
                stopWaypointMission();
                break;
            }
            default:
                break;
        }
    }

